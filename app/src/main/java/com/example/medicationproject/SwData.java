package com.example.medicationproject;

public class SwData {

    public boolean on1;
    public boolean on2;
    public boolean on3;
    public boolean off;

    public SwData() {
        this.off = off;
    }

    public boolean isOn1() {
        return on1;
    }

    public void setOn1(boolean on1) {
        this.on1 = on1;
    }

    public boolean isOn2() {
        return on2;
    }

    public void setOn2(boolean on2) {
        this.on2 = on2;
    }

    public boolean isOn3() {
        return on3;
    }

    public void setOn3(boolean on3) {
        this.on3 = on3;
    }

    public boolean isOff() {
        return off;
    }

    public void setOff(boolean off) {
        this.off = off;
    }
    public void Swich1() {
        this.on1 = on1;
    }
    public void Swich2() {
        this.on2 = on2;
    }
    public void Swich3() {
        this.on3 = on3;
    }
}
